package chapter04;

public class StringTest01 {

	public static void main(String[] args) {
		// "c:\temp" 를 출력해보자
		
		System.out.println("c:\temp"); //c:	emp
		System.out.println("c:\\temp");
		
		// \t: tab
		// \r: carriage return 
		// \n: newline
		// \b: beep
		
		// "hello" 를 출력해보자
		System.out.println("\"hello\"");
		
		// '
		char c = '\'';
		System.out.println("\'");
		System.out.println(c);
	}

}
